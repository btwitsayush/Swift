
func basics_01(){
    
//    The naming convention in swift will be camelCase
    
    /*
     keywords in swift which are used to declare variables
     */
//   if we declare something using let than it's mean we can't update the value of that varaible basically they are immutable
    
    let name:String="Ayush Srivastava"
    print(name)
    
//  if we declare something in var than we can reintialize it's value as well
    var userName:String="Aaayushhh"
    userName="Ayush"
    print(userName)
}

